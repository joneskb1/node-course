exports.getTourStats = catchAsync(async (req, res, next) => {
  const stats = await Tour.aggregate([
    {
      $match: { ratingsAverage: { $gte: 4.5 } },
    },
    {
      $group: {
        _id: { $toUpper: '$difficulty' },
        numRatings: { $sum: '$ratingsQuantity' },
        numTours: { $sum: 1 }, //counts per doc
        avgRating: { $avg: '$ratingsAverage' },
        avgPrice: { $avg: '$price' },
        minPrice: { $min: '$price' },
        maxPrice: { $max: '$price' },
      },
    },
    {
      $sort: { avgPrice: 1 }, //ascending;
    },
  ]);

  res.status(200).json({
    status: 'success',
    data: {
      stats,
    },
  });
});

exports.getMonthlyPlan = catchAsync(async (req, res, next) => {
  const year = req.params.year * 1;
  const plan = await Tour.aggregate([
    {
      //creates one doc per item in field array
      $unwind: '$startDates',
    },
    {
      $match: {
        startDates: {
          // filters only the year param
          $gte: new Date(`${year}-01-01`),
          $lte: new Date(`${year}-12-31`),
        },
      },
    },
    {
      $group: {
        // creats a month from the date
        _id: { $month: '$startDates' },
        numTourStarts: { $sum: 1 },
        // create an array of names with push
        tours: { $push: '$name' },
      },
    },
    {
      // add field month = to id
      $addFields: { month: '$_id' },
    },
    {
      $project: {
        // remove _id field
        _id: 0,
      },
    },
    {
      $sort: {
        numTourStarts: -1, //descending
      },
    },
    {
      $limit: 12, //limits # of output data
    },
  ]);
  res.status(200).json({
    status: 'success',
    data: {
      plan,
    },
  });
});

exports.getToursWithin = catchAsync(async (req, res, next) => {
  const { distance, latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');

  //mongo need radiance (distance/radius of earth)
  const radius = unit === 'mi' ? distance / 3963.2 : distance / 6378.1;

  if (!lat || !lng) {
    next(new AppError('need lat & lng', 400));
  }

  const tours = await Tour.find({
    startLocation: {
      $geoWithin: {
        $centerSphere: [[lng, lat], radius],
      },
    },
  });

  res.status(200).json({
    status: 'success',
    results: tours.length,
    data: {
      data: tours,
    },
  });
});

exports.getDistances = catchAsync(async (req, res, next) => {
  const { latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');

  const multiplier = unit === 'mi' ? 0.000621371 : 0.001;

  if (!lat || !lng) {
    next(new AppError('need lat & lng', 400));
  }

  const distances = await Tour.aggregate([
    {
      //geoNear must be first
      //if you have a geo index it automatically pulls it in (if not must add it)
      $geoNear: {
        // must have near and a variable
        near: {
          type: 'Point',
          coordinates: [lng * 1, lat * 1],
        },
        //store values in this field
        distanceField: 'distance',
        distanceMultiplier: multiplier,
      },
    },
    {
      // only return distance and name fields
      $project: {
        distance: 1,
        name: 1,
      },
    },
  ]);

  res.status(200).json({
    status: 'success',
    data: {
      data: distances,
    },
  });
});
